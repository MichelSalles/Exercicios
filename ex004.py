n = input('Digite algo: ')
print(n)
print('O tipo primitivo desse valor é', type(n))
print('Só tem espaços? ',n.isspace())
print('Está em maiúsculo? ',n.isupper())
print('Está em minusculas ?',n.islower())
print('É alphanumerico? ',n.isalnum())
print('Está capitalizada? Estas maiuscula e minuscula ? ',n.istitle())

